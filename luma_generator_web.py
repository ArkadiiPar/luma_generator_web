# --- –í—Å–ø–æ–º–æ–≥–∞—Ç–µ–ª—å–Ω—ã–µ —Ñ—É–Ω–∫—Ü–∏–∏ ---
def float_to_hex(f):
    return struct.pack('<f', f).hex()

def hex_to_float(hex_str):
    if len(hex_str) < 8:
        return 0.0
    try:
        return struct.unpack('<f', bytes.fromhex(hex_str[:8]))[0]
    except:
        return 0.0


